def add(num, num1)
	value = num + num1
	return value
end


def subtract(num, num1)
	value = num - num1
	return value
end


def sum(num)
	value = 0
	num.each {|num| value += num}
	return value
end


def multiply(num1, num2)
	value = num1 * num2
	return value
end

def power(num, num1)
	value = num ** num1
	return value
end

